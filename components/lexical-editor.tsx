function onError(error: Error) {
  console.error(error);
}

function Editor() {
  const initialConfig = {
    namespace: "QEDEditor",
    theme: editorTheme,
    onError,
    nodes: [
      HeadingNode,
      ListNode,
      ListItemNode,
      QuoteNode,
      CodeNode,
      CodeHighlightNode,
      TableNode,
      TableCellNode,
      TableRowNode,
      AutoLinkNode,
      LinkNode,
    ],
  };

  return (
    <LexicalComposer initialConfig={initialConfig}>
      <div className="editor-container relative w-full h-full">
        <RichTextPlugin
          contentEditable={
            <ContentEditable className="editor-input w-full h-full p-4 outline-none" />
          }
          placeholder={
            <div className="editor-placeholder absolute top-4 left-4 text-gray-500 pointer-events-none">
              Start writing...
            </div>
          }
          ErrorBoundary={LexicalErrorBoundary}
        />
        <HistoryPlugin />
      </div>
    </LexicalComposer>
  );
}

export default function LexicalEditor() {
  return <Editor />;
}
